public class FOS {
    private String name;
    private int w;
    FOS(String name , int w){
        this.name = name;
        this.w = w;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }
}
